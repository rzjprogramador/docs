/* FUNCAO QUE RECEBE DOIS NUMEROS E RETORNA O MAIOR ENTRE ELES */
//
function numeroMaior(num1, num2) {
  if (num1 > num2) {
    return `Enviados ${num1} e ${num2} :: O MAIOR É ${num1}`;
  }
  return `Enviados ${num1} e ${num2} :: O MAIOR É ${num2}`;
}

console.log(numeroMaior(1, 200));

